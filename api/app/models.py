from sqlalchemy import Column, String, DateTime
from api.app.infrastructure.database import Base

class Task(Base):
    """
    Represents a single task in the task management system.

    Columns
    -------
    id      : UUID string, primary key. Generated by the service layer.
    title   : Human-readable task description. Required.
    status  : Task status. Defaults to 'todo'.
              Accepted values: 'todo', 'in_progress', 'done'.
    due_date : Optional deadline date for the task.
    """
    __tablename__ = "tasks"

    id = Column(String, primary_key=True, index=True)
    title = Column(String, nullable=False)
    status = Column(String, default="todo")
    due_date = Column(DateTime, nullable=True)
