from datetime import datetime

from sqlalchemy import String, DateTime
from sqlalchemy.orm import Mapped, mapped_column
from src.infrastructure.database import Base

class Task(Base):
    """
    Represents a single task in the task management system

    Columns
    -------
    id      : UUID string, primary key. Generated by the service layer
    title   : Human-readable task description. Required
    status  : Task status. Defaults to 'todo'
              Accepted values: 'todo', 'in_progress', 'done'
    due_date : Optional deadline date for the task
    """
    __tablename__ = "tasks"

    id: Mapped[str] = mapped_column(String, primary_key=True, index=True)
    title: Mapped[str] = mapped_column(String, nullable=False)
    status: Mapped[str] = mapped_column(String, default="todo")
    due_date: Mapped[datetime | None] = mapped_column(DateTime, nullable=True)